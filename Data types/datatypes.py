# integer type
x = 5
print(x)
print(type(x))
# string type
x = "smit"
print(x)
print(type(x))
# float type
x = 10.5
print(x)
print(type(x))
# complex type
x = 2j
print(x)
print(type(x))
# list type
x = ["apple","mango","banana"]
print(x)
print(type(x))
# tupple type
x = ("apple","mango","banana")
print(x)
print(type(x))
# set type
x = {"apple","mango","banana"}
print(x)
print(type(x))
# dictionary type
x = {"name" : "Smit", "age" : 23, "height": 5.5}
print(x)
print(type(x))
# boolean type
x = True
print(x)
print(type(x))
